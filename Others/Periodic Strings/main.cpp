vector<int> prefix_function(string& s) {
    int n = (int)s.length();
    vector<int> pi(n);
    for (int i = 1; i < n; i++) {
        int j = pi[i-1];
        while (j > 0 && s[i] != s[j])
            j = pi[j-1];
        if (s[i] == s[j])
            j++;
        pi[i] = j;
    }
    return pi;
}


void solve(int t){
    string s; cin>>s;
    int n = s.size();
    vector<int> pi = prefix_function(s);
    int k = n - pi[n-1];
    if(n%k == 0) cout << k << endl;
    else cout << n << endl;
    if(t != 0) cout << endl;
}

